    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:

        int baris = table_nilai.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang mau dihapus di tabel!");
            return;
        }

        String status = modelNilai.getValueAt(baris, 5).toString();
        String idNilai = modelNilai.getValueAt(baris, 0).toString();

        if (status.equals("Draft")) {
            modelNilai.removeRow(baris);
            cb_nimActionPerformed(null);
        } else {
            int konfirmasi = JOptionPane.showConfirmDialog(this, "Hapus nilai ini dari database?", "Hapus", JOptionPane.YES_NO_OPTION);
            if (konfirmasi == JOptionPane.YES_OPTION) {
                try {
                    Connection conn = KoneksiDatabase.dB_koneksi();
                    PreparedStatement pst = conn.prepareStatement("DELETE FROM nilai WHERE id_nilai=?");
                    pst.setString(1, idNilai);
                    pst.execute();

                    JOptionPane.showMessageDialog(this, "Nilai terhapus dari Database!");
                    cb_nimActionPerformed(null); 
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus: " + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

