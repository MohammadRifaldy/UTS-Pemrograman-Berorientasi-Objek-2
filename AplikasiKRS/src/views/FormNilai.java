    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:

        if (modelNilai.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Tabel masih kosong!");
            return;
        }

        try {
            Connection conn = KoneksiDatabase.dB_koneksi();
            String sql = "INSERT INTO nilai (nim, kode_mk, semester_diambil, nilai_huruf, nilai_angka) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);

            String nim = cb_nim.getSelectedItem().toString();
            int jumlahDraft = 0;

  
            for (int i = 0; i < modelNilai.getRowCount(); i++) {
                String status = modelNilai.getValueAt(i, 5).toString();

             
                if (status.equals("Draft")) {
                    pst.setString(1, nim);
                    pst.setString(2, modelNilai.getValueAt(i, 1).toString());
                    pst.setString(3, modelNilai.getValueAt(i, 2).toString()); 
                    pst.setString(4, modelNilai.getValueAt(i, 3).toString()); 
                    pst.setString(5, modelNilai.getValueAt(i, 4).toString()); 
                    pst.addBatch();

                    modelNilai.setValueAt("Tersimpan", i, 5);
                    jumlahDraft++;
                }
            }

            if (jumlahDraft > 0) {
                pst.executeBatch(); 
                JOptionPane.showMessageDialog(this, jumlahDraft + " nilai berhasil disimpan ke Database!");

                cb_nimActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada nilai baru (Draft) yang perlu disimpan.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan ke DB: " + e.getMessage());
        }


    }//GEN-LAST:event_btn_saveActionPerformed

