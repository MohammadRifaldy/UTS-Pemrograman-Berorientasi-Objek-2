    private void cb_nimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_nimActionPerformed
        // TODO add your handling code here:

        if (cb_nim.getSelectedIndex() > 0) {
            try {
                Connection conn = KoneksiDatabase.dB_koneksi();
                String nimDipilih = cb_nim.getSelectedItem().toString();

              
                String sqlMhs = "SELECT nama, semester_aktif FROM mahasiswa WHERE nim = ?";
                PreparedStatement pstMhs = conn.prepareStatement(sqlMhs);
                pstMhs.setString(1, nimDipilih);
                ResultSet rsMhs = pstMhs.executeQuery();
                if (rsMhs.next()) {
                    tf_kolomnama.setText(rsMhs.getString("nama"));
                    tf_kolomsemester.setText(rsMhs.getString("semester_aktif"));
                }
                rsMhs.close();
                pstMhs.close();

                modelNilai.setRowCount(0);
                String sqlNilai = "SELECT * FROM nilai WHERE nim = ?";
                PreparedStatement pstNilai = conn.prepareStatement(sqlNilai);
                pstNilai.setString(1, nimDipilih);
                ResultSet rsNilai = pstNilai.executeQuery();
                while (rsNilai.next()) {
                    modelNilai.addRow(new Object[]{
                        rsNilai.getString("id_nilai"),
                        rsNilai.getString("kode_mk"),
                        rsNilai.getString("semester_diambil"),
                        rsNilai.getString("nilai_huruf"),
                        rsNilai.getString("nilai_angka"),
                        "Tersimpan"
                    });
                }
                rsNilai.close();
                pstNilai.close();

   
                String sqlMatkul = "SELECT k.kode_mk, m.nama_mk FROM krs k JOIN mata_kuliah m ON k.kode_mk = m.kode_mk "
                        + "WHERE k.nim = ? AND k.kode_mk NOT IN (SELECT kode_mk FROM nilai WHERE nim = ?)";
                PreparedStatement pstMatkul = conn.prepareStatement(sqlMatkul);
                pstMatkul.setString(1, nimDipilih);
                pstMatkul.setString(2, nimDipilih); 
                ResultSet rsMatkul = pstMatkul.executeQuery();

                cb_matkul.removeAllItems();
                cb_matkul.addItem("-- Pilih Matkul Kontrak --");
                while (rsMatkul.next()) {
                    cb_matkul.addItem(rsMatkul.getString("kode_mk") + " - " + rsMatkul.getString("nama_mk"));
                }
                rsMatkul.close();
                pstMatkul.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error load data: " + e.getMessage());
            }
        } else {
            tf_kolomnama.setText("");
            tf_kolomsemester.setText("");
            cb_matkul.removeAllItems();
            if (modelNilai != null) {
                modelNilai.setRowCount(0);
            }
        }
    }//GEN-LAST:event_cb_nimActionPerformed

