    private void cb_nimpendingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_nimpendingActionPerformed
        // TODO add your handling code here:
        if (cb_nimpending.getSelectedIndex() > 0) {
            try {
                Connection conn = KoneksiDatabase.dB_koneksi();
                String nim = cb_nimpending.getSelectedItem().toString();

                String sqlMhs = "SELECT nama, semester_aktif FROM mahasiswa WHERE nim = ?";
                PreparedStatement pstMhs = conn.prepareStatement(sqlMhs);
                pstMhs.setString(1, nim);
                ResultSet rsMhs = pstMhs.executeQuery();
                if (rsMhs.next()) {
                    tf_kolomnama.setText(rsMhs.getString("nama"));
                    tf_kolomsemester.setText(rsMhs.getString("semester_aktif"));
                }
                rsMhs.close();
                pstMhs.close();

                modelReview.setRowCount(0);
                int totalSks = 0;

                String sqlKrs = "SELECT k.kode_mk, m.nama_mk, m.sks, k.status_dospen, k.status_kaprodi FROM krs k JOIN mata_kuliah m ON k.kode_mk = m.kode_mk WHERE k.nim = ? AND (k.status_dospen = 'pending' OR k.status_kaprodi = 'pending')";
                PreparedStatement pstKrs = conn.prepareStatement(sqlKrs);
                pstKrs.setString(1, nim);
                ResultSet rsKrs = pstKrs.executeQuery();

                while (rsKrs.next()) {
                    String kode = rsKrs.getString("kode_mk");
                    String namaMk = rsKrs.getString("nama_mk");
                    int sks = rsKrs.getInt("sks");

                    String statusTampil = "Dospen: " + rsKrs.getString("status_dospen") + " | Kaprodi: " + rsKrs.getString("status_kaprodi");

                    modelReview.addRow(new Object[]{kode, namaMk, sks, statusTampil});
                    totalSks += sks;
                }
                rsKrs.close();
                pstKrs.close();

                tf_kolomtotalSks.setText(String.valueOf(totalSks) + " SKS");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error load data review: " + e.getMessage());
            }
        } else {

            tf_kolomnama.setText("");
            tf_kolomsemester.setText("");
            tf_kolomsemester.setText("");
            if (modelReview != null) {
                modelReview.setRowCount(0);
            }
        }
    }//GEN-LAST:event_cb_nimpendingActionPerformed

    private void btn_accActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accActionPerformed
        // TODO add your handling code here:

        if (cb_nimpending.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Silakan pilih mahasiswa terlebih dahulu!");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menyetujui (" + tf_kolomtotalSks.getText() + ") KRS mahasiswa ini?", "Konfirmasi ACC", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Connection conn = KoneksiDatabase.dB_koneksi();
                String nim = cb_nimpending.getSelectedItem().toString();

     
                String sql = "UPDATE krs SET status_dospen = 'ACC', status_kaprodi = 'ACC' WHERE nim = ? AND (status_dospen = 'pending' OR status_kaprodi = 'pending')";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, nim);
                pst.execute();

                JOptionPane.showMessageDialog(this, "KRS Berhasil Di-ACC Sepenuhnya!");

                loadNimPending(); 
                tf_kolomnama.setText("");
                tf_kolomsemester.setText("");
                tf_kolomtotalSks.setText("");
                modelReview.setRowCount(0);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal ACC KRS: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_btn_accActionPerformed

    private void btn_tolakkrsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tolakkrsActionPerformed
        // TODO add your handling code here:

        if (cb_nimpending.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Silakan pilih mahasiswa terlebih dahulu!");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(this, "Tolak dan kembalikan KRS mahasiswa ini?", "Konfirmasi Tolak", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Connection conn = KoneksiDatabase.dB_koneksi();
                String nim = cb_nimpending.getSelectedItem().toString();

                String sql = "DELETE FROM krs WHERE nim = ? AND (status_dospen = 'pending' OR status_kaprodi = 'pending')";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, nim);
                pst.execute();

                JOptionPane.showMessageDialog(this, "KRS Ditolak. Mahasiswa harus menginput ulang.");

                loadNimPending();
                tf_kolomnama.setText("");
                tf_kolomsemester.setText("");
                tf_kolomtotalSks.setText("");
                modelReview.setRowCount(0);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal Menolak KRS: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_tolakkrsActionPerformed


