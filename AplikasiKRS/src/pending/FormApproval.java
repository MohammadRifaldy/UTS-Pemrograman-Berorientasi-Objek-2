    private void loadNimPending() {
        try {
            Connection conn = KoneksiDatabase.dB_koneksi();
         
            String sql = "SELECT DISTINCT nim FROM krs WHERE status_dospen = 'pending' OR status_kaprodi = 'pending'";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            cb_nimpending.removeAllItems();
            cb_nimpending.addItem("-- Pilih Mahasiswa --");

            while (rs.next()) {
                cb_nimpending.addItem(rs.getString("nim"));
            }
            rs.close();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error load NIM Pending: " + e.getMessage());
        }
    }

